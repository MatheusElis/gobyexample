/* 
Go tem varios tipos de valores incluindo string, integers, floats, booleans e etc

Aqui temos alguns exemplos basicos.

*/
package main

import "fmt"

func main() {
  fmt.Println("go" + "lang")
  
  fmt.Println("1+1 = ", 1+1)
  fmt.Println("7.0/3.0 = ", 7.0/3.0)

  fmt.Println(true && false)
  fmt.Println(true || false)
  fmt.Println(!true)
}
